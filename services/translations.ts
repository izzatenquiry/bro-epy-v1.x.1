export const getTranslations = (language: Language) => {
    const appName = MASTER_CONFIG.appName || 'AI Platform';
    const registrationUrl = MASTER_CONFIG.registrationUrl || '#';
    const rawTranslations = translations[language] || translations.en;

    // Recursive function to replace placeholders
    const replacePlaceholders = (obj: any): any => {
        if (typeof obj === 'string') {
            return obj.replace(/{appName}/g, appName).replace(/{registrationUrl}/g, registrationUrl);
        }
        if (Array.isArray(obj)) {
            return obj.map(item => replacePlaceholders(item));
        }
        if (typeof obj === 'object' && obj !== null) {
            const newObj: { [key: string]: any } = {};
            for (const key in obj) {
                if (Object.prototype.hasOwnProperty.call(obj, key)) {
                    newObj[key] = replacePlaceholders(obj[key]);
                }
            }
            return newObj;
        }
        return obj;
    };

    return replacePlaceholders(rawTranslations);
};